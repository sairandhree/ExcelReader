package com.saka.excelreader.core;

public class InputOutput {
	
	Input input;
	Input output;
		
	public InputOutput()
	{
	}
	public Input getInput() {
		return input;
	}

	public void setInput(Input input) {
		this.input = input;
	}

	public Input getOutput() {
		return output;
	}

	public void setOutput(Input output) {
		this.output = output;
	}

	@Override
	public String toString() {
		return "InputOutput [input=" + input + ", output=" + output + "]";
	}

	
	
	 
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
